    <h1>About Assurify</h1>
    
    <p><strong>Assurify</strong> is a community platform for sharing and discussing technology stories, particularly focused on information security, technology infrastructure, and business assurance. We provide a space for professionals to share links to interesting articles, engage in thoughtful discussions, and learn from each other's expertise.</p>
    
    <h2>Community Guidelines</h2>
    
    <p>This is intended to be a civil, well-lit place for discussion. The following behaviors are encouraged:</p>
    
    <ul>
        <li><strong>Be respectful</strong> - Treat other community members with courtesy and professionalism</li>
        <li><strong>Stay on topic</strong> - Keep discussions relevant to technology, security, and business assurance</li>
        <li><strong>Provide context</strong> - When sharing links, include a brief description of why it's interesting</li>
        <li><strong>Fact-check</strong> - Verify information before sharing, and cite sources when possible</li>
        <li><strong>Learn from others</strong> - Ask questions and share knowledge constructively</li>
    </ul>
    
    <p>The following behaviors are not tolerated:</p>
    
    <ul>
        <li>Personal attacks, harassment, or abusive language</li>
        <li>Spam, self-promotion without value, or off-topic content</li>
        <li>Sharing of malicious code or links</li>
        <li>Doxxing or sharing private information</li>
        <li>Trolling or deliberately inflammatory content</li>
    </ul>
    
    <h2>Tags and Content Organization</h2>
    
    <p>Stories are organized using tags to help categorize content. When submitting stories, please use appropriate tags to help other users find relevant content. You can view all available tags on the <a href="/tags">Tags page</a>.</p>
    
    <p>Users can filter content based on tags using the <a href="/filter">Filter page</a> to customize their feed according to their interests.</p>
    
    <h2>Invitation System</h2>
    
    <p>Assurify operates on an invitation-only basis to maintain community quality. New users must be invited by existing community members. The invitation tree can be viewed on the <a href="/users">Users page</a>, which shows the relationship between community members.</p>
    
    <p>Inviting users is a responsibility - please only invite people who you believe will contribute positively to the community discussions.</p>
    
    <h2>Story Ranking and Voting</h2>
    
    <p>Stories are ranked based on a combination of factors including:</p>
    
    <ul>
        <li><strong>Vote score</strong> - The net positive votes from community members</li>
        <li><strong>Time since submission</strong> - Newer stories are given priority</li>
        <li><strong>Comment engagement</strong> - Stories with active discussions may stay visible longer</li>
    </ul>
    
    <p>Users accumulate karma based on the votes their stories and comments receive from other community members.</p>
    
    <h2>Transparency and Moderation</h2>
    
    <p>Community moderation is transparent and accountable. All moderation actions are logged and visible on the <a href="/moderations">Moderation Log</a>. This ensures that moderation decisions are fair and consistent with community guidelines.</p>
    
    <p>Moderators and administrators are community volunteers who help maintain the quality of discussions and ensure adherence to community guidelines.</p>
    
    <h2>Privacy and Data</h2>
    
    <p>We respect user privacy and are committed to protecting your personal information. We do not sell user data or use it for advertising purposes. User profiles can be configured to control what information is publicly visible.</p>
    
    <h2>Technical Information</h2>
    
    <p>Assurify is built using modern web technologies and is designed to be fast, secure, and accessible. The platform supports:</p>
    
    <ul>
        <li>Responsive design for mobile and desktop usage</li>
        <li>Progressive Web App (PWA) functionality for offline reading</li>
        <li>Real-time notifications for messages and mentions</li>
        <li>Advanced search and filtering capabilities</li>
        <li>RSS feeds for story updates</li>
    </ul>
    
    <h2>Contact and Feedback</h2>
    
